/**
 * 「{都市} ベストシーズン」ページ用の追加データ
 *
 * - 月別の "おすすめ度"（ベスト / 良い / 普通 / 避ける）
 * - その都市ならではの月ごとの見どころ（イベント・気候）
 * - 服装/持ち物ヒント
 * - 価格傾向（ハイシーズン/オフシーズン）
 *
 * hotel-destinations.ts の bestSeason / hotel-city-guides.ts の climate と
 * 補完し合うコンテンツ。SEO狙いは「{都市} ベストシーズン」「{都市} 何月」
 * 「{都市} 安い時期」「{都市} 旅行 おすすめ時期」など long-tail。
 */

export type MonthRating = "best" | "good" | "ok" | "avoid";

export type WhenToVisitContent = {
  /** 1月-12月のそれぞれの推奨度 */
  monthRatings: Record<number, MonthRating>;
  /** 月別のひと言コメント（観光・気候・イベント） — 12個 */
  monthNotes: Record<number, string>;
  /** "総合"のベスト月。日本語の月名で 2-3 個 */
  bestMonthsLabel: string;
  /** 安く行ける月（オフシーズン）。日本語 */
  cheapMonthsLabel: string;
  /** 避けるべき月（観光に不向きな時期）。空文字なら表示しない */
  avoidMonthsLabel: string;
  /** 服装・持ち物のヒント */
  packingTips: string[];
  /** その都市の年間イベントハイライト（3-5個） */
  yearlyHighlights: string[];
};

/**
 * 簡略表記用ヘルパ:
 *   B=best, G=good, O=ok, A=avoid を1月から12月まで12文字で。
 */
function parseRating(code: string): Record<number, MonthRating> {
  const map: Record<string, MonthRating> = {
    B: "best",
    G: "good",
    O: "ok",
    A: "avoid",
  };
  const out: Record<number, MonthRating> = {};
  for (let i = 0; i < 12; i++) {
    out[i + 1] = map[code[i] ?? "O"] ?? "ok";
  }
  return out;
}

