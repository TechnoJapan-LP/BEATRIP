/**
 * その都市の when-to-visit 詳細データが整備済みかどうか。
 * false の都市は best-season ページを noindex + sitemap 非掲載にする。
 */
export function hasWhenToVisitContent(slug: string): boolean {
  return slug in RAW;
}

export function getWhenToVisitContent(slug: string): WhenToVisitContent {
  const r = RAW[slug] ?? FALLBACK;
  return {
    monthRatings: parseRating(r.ratings),
    monthNotes: Object.fromEntries(
      r.notes.map((n, i) => [i + 1, n])
    ) as Record<number, string>,
    bestMonthsLabel: r.bestMonthsLabel,
    cheapMonthsLabel: r.cheapMonthsLabel,
    avoidMonthsLabel: r.avoidMonthsLabel,
    packingTips: r.packingTips,
    yearlyHighlights: r.yearlyHighlights,
  };
}

